/*关系运算符：
>
>=
<
<=
==   等于
!=   不等于
=    是赋值运算符
==   是关系运算符

关系运算符运行结果一定是布尔类型：true/false

*/


public class OperatorTest02
{
    public static void main(String[]args){
        int a = 10;
        int b = 10;
        System.out.println(a > b);  
        System.out.println(a >= b);  
        System.out.println(a < b);  
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}